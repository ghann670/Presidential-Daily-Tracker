// Helper functions
function getAllDates() {
    return Object.keys(scheduleData).sort();
}

function getEventsForDate(date) {
    return scheduleData[date]?.events || [];
}

function getDatesWithEvents() {
    return Object.keys(scheduleData).filter(date => scheduleData[date].events.length > 0);
}

function getLatestDate() {
    const dates = getDatesWithEvents();
    return dates[dates.length - 1];
}
